//------------------------------------------------------------------------------
// Odometer : class definition
//------------------------------------------------------------------------------
#include "Odometer.h"

//------------------------------------------------------------------------------
// default constructor
//------------------------------------------------------------------------------
Odometer::Odometer() : Odometer(0.0) {}

//------------------------------------------------------------------------------
// constructor
//------------------------------------------------------------------------------
Odometer::Odometer(float initialMiles) { milesDriven = initialMiles; }

//------------------------------------------------------------------------------
// returns odometer reading (number of miles driven)
//------------------------------------------------------------------------------
float Odometer::getMilesDriven() const { return milesDriven; }

//------------------------------------------------------------------------------
// adds miles to the odometer reading (number of miles driven)
//------------------------------------------------------------------------------
void Odometer::addMilesDriven(float miles) { milesDriven += miles; };
